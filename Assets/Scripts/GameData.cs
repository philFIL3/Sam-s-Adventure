using System.Collections;
using System.Collections.Generic;
using UnityEngine;

/// <summary>
/// Blank GameData for LoadSave to run against.
/// _value can be replaced with a custom data object and values plucked in the constructor.
/// </summary>
public class GameData
{
    public int aValue;

    public GameData(int _value)
    {
        aValue = _value;
    }
}
